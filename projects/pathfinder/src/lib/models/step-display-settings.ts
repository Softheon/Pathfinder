/** The step display settings */
export class StepDisplaySettings {
    /** The group id for the step */
    public group: string;

    /** The level to be displayed in the tree view */
    public level: number;

    /** The title to be used for the step */
    public title: string;

    /** The tooltip for the step */
    public tooltip: string;

    /** True if the step has children */
    public hasChildren: boolean;
}